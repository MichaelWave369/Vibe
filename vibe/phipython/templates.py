from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class PythonTemplate:
    """Project scaffold template entry."""

    name: str
    description: str
    files: dict[str, str]


def _base_readme(template_name: str, project_name: str, description: str) -> str:
    return (
        f"# {project_name}\n\n"
        f"Generated by PhiPython template `{template_name}`.\n\n"
        f"{description}\n\n"
        "## Quickstart\n\n"
        "```bash\n"
        "python -m pip install -r requirements.txt\n"
        "python main.py\n"
        "```\n\n"
        "Scope note: this is a bounded starter kit intended for local learning and iteration.\n"
    )


