def list_templates() -> list[PythonTemplate]:
    return [_TEMPLATE_REGISTRY[key] for key in sorted(_TEMPLATE_REGISTRY)]


def get_template(name: str) -> PythonTemplate | None:
    return _TEMPLATE_REGISTRY.get(name)


def template_as_dict(name: str) -> dict[str, object]:
    template = get_template(name)
    if template is None:
        raise KeyError(f"unknown template: {name}")
    return {
        "name": template.name,
        "description": template.description,
        "files": dict(sorted(template.files.items())),
    }


def render_template_files(template_name: str, project_name: str) -> dict[str, str]:
    template = get_template(template_name)
    if template is None:
        raise KeyError(f"unknown template: {template_name}")
    files = dict(template.files)
    files["README.md"] = _base_readme(template_name, project_name, template.description)
    return files
